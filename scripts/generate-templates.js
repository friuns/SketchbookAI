const fs = require('fs');
const path = require('path');

const examplesDir = path.join(__dirname, '../src/main/examples');
const templatesFile = path.join(__dirname, '../src/ts/world/templates.ts');

const files = fs.readdirSync(examplesDir)
  .filter(file => file.endsWith('.ts') || file.endsWith('.js'));

const content = `// This file is generated by scripts/generate-templates.js
export const templates = ${JSON.stringify(files, null, 2)};
`;

fs.writeFileSync(templatesFile, content);

console.log(`Successfully generated ${templatesFile}`);
